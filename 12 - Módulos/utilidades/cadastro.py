# Função Principal

def pessoas():
  dicio, lista = {}, []
  opcao = menu()
  while opcao != 3:
    if opcao == 2:
      while True:
        dicio['nome'] = nome()
        dicio['idade'] = idade()
        lista.append(dicio.copy())
        dicio.clear()
        continuar = SoN('Deseja adcionar mais uma pessoa? ')
        if continuar == 'N':
          break
      opcao = menu()
    if opcao == 1:
      print('-'*40)
      print(f'{"PESSOAS CADASTRADAS":^40}')
      print('-'*40)
      for p in lista:
        print(f'{p["nome"]:<30}{(str(p["idade"])+" anos"):^10}')
      opcao = menu()
  if opcao == 3:
    printcolorido('Programa finalizado.','azul')



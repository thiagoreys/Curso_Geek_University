def nome():
  while True:
    nome = input('Nome: ').title()
    nome = ' '.join(nome.split())
    if nome.replace(' ','').isalpha():
      return nome
    printcolorido('Digite um nome válido.','vermelho')

def idade():
  while True:
    idade = input('Idade: ').replace(' ','')
    try:
      int(idade)
      return int(idade)
    except:
      printcolorido('Digite uma idade válida.','vermelho')


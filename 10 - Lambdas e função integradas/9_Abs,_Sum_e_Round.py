# Abs -> Retorno o valor absoluto (módulo).

print(abs(-5))
print(abs(5))

# Sum -> Retorna a soma dos valores de um iterável.

print(sum([1,2,3,4,5]))
print(sum([1,2,3,4,5],5)) # Aceita um 'valor inicial' fora do iterável.

# Round -> Retorna um número arrendondado.

print(round(3.7681))
print(round(3.7681, 2)) # Você pode informar a quantidade de casas decimais.